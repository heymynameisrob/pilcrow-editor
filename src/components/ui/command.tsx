import * as React from "react";
import { type DialogProps } from "@radix-ui/react-dialog";
import { Command as CommandPrimitive } from "cmdk";
import { Search } from "lucide-react";

import { cn } from "@/utils";
import { CommandDialog as Dialog } from "@/components/ui/dialog";
import { Badge } from "@/components/ui/badge";

const Command = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    className={cn(
      "dark flex h-full w-full flex-col overflow-hidden rounded-md bg-black text-white",
      className,
    )}
    {...props}
  />
));
Command.displayName = CommandPrimitive.displayName;

type CommandDialogProps = DialogProps;

const CommandDialog = React.forwardRef(
  (
    { children, ...props }: CommandDialogProps,
    ref: React.Ref<HTMLDivElement> | null,
  ) => {
    return (
      <Dialog {...props}>
        <Command
          ref={ref}
          className="[&_[cmdk-group-heading]]:px-2 [&_[cmdk-group-heading]]:font-medium [&_[cmdk-group-heading]]:text-muted [&_[cmdk-group]:not([hidden])_~[cmdk-group]]:pt-0 [&_[cmdk-group]]:px-2 [&_[cmdk-input-wrapper]_svg]:w-5 [&_[cmdk-input]]:h-12 [&_[cmdk-item]]:px-2 [&_[cmdk-item]]:py-3 rounded-md border border-white/15 shadow-2xl transition-all ease-in-out"
        >
          {children}
        </Command>
      </Dialog>
    );
  },
);
CommandDialog.displayName = "CommandDialog";

const CommandInput = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive.Input>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive.Input> & {
    activePage?: string | null;
  }
>(({ className, activePage, ...props }, ref) => (
  <div
    className="flex items-center bg-neutral-900 border-b border-white/10 px-3 bg-ui-low"
    cmdk-input-wrapper=""
  >
    <Search className="mr-2 h-4 w-4 shrink-0 opacity-50" />
    {activePage && <Badge className="mr-2 capitalize">{activePage}</Badge>}
    <CommandPrimitive.Input
      ref={ref}
      className={cn(
        "flex h-11 w-full rounded-md bg-transparent py-3 text-sm outline-none placeholder:text-muted disabled:cursor-not-allowed disabled:opacity-50",
        className,
      )}
      {...props}
    />
  </div>
));

CommandInput.displayName = CommandPrimitive.Input.displayName;

